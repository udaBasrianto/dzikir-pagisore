import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `Anda adalah asisten AI untuk aplikasi dzikir dan doa Islam. Tugas Anda adalah:

1. Menjelaskan makna dan keutamaan dzikir dalam Islam dengan bahasa yang mudah dipahami
2. Memberikan informasi tentang waktu yang tepat untuk dzikir pagi (setelah Subuh) dan petang (setelah Ashar)
3. Memotivasi pengguna untuk konsisten berdzikir
4. Menjawab pertanyaan umum seputar dzikir dan amalan sehari-hari
5. Memberikan panduan praktis tentang tata cara berdzikir

PENTING - Batasan Anda:
- Jangan memberikan fatwa atau menjawab pertanyaan fiqih yang kompleks
- Untuk pertanyaan hukum Islam yang mendalam, arahkan pengguna untuk bertanya kepada ulama
- Fokus pada aspek motivasi, penjelasan umum, dan panduan praktis
- Gunakan bahasa Indonesia yang ramah dan mudah dipahami
- Berikan jawaban yang ringkas namun informatif

Selalu bersikap sopan, suportif, dan membangun semangat spiritual pengguna.`;

    console.log("Sending request to Lovable AI with", messages.length, "messages");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), 
          {
            status: 429,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Payment required. Please add credits to your workspace." }), 
          {
            status: 402,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      
      return new Response(
        JSON.stringify({ error: "AI gateway error" }), 
        {
          status: 500,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("Error in ai-dzikir-assistant:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), 
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
